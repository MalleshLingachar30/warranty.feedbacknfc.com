#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";

const PROJECT_ROOT = process.cwd();
const INDIA_SOURCE_PATH = path.join(PROJECT_ROOT, "index.html");
const SAUDI_ENGLISH_SOURCE_PATH = path.join(PROJECT_ROOT, "sa-en.html");
const SAUDI_ARABIC_SOURCE_PATH = path.join(PROJECT_ROOT, "ar.html");
const OUTPUT_DIR = path.join(PROJECT_ROOT, "src", "lib", "generated");
const OUTPUT_PATH = path.join(OUTPUT_DIR, "landing-page-content.ts");

function buildModuleSource(indiaHtml, saudiEnglishHtml, saudiArabicHtml) {
  return `// Auto-generated by scripts/generate-landing-content.mjs.
// Do not edit this file manually.

export const LANDING_PAGE_HTML = {
  in: {
    en: ${JSON.stringify(indiaHtml)},
  },
  sa: {
    en: ${JSON.stringify(saudiEnglishHtml)},
    ar: ${JSON.stringify(saudiArabicHtml)},
  },
} as const;
`;
}

async function writeIfChanged(filePath, content) {
  let existing = null;
  try {
    existing = await fs.readFile(filePath, "utf8");
  } catch (error) {
    if (error && error.code !== "ENOENT") {
      throw error;
    }
  }

  if (existing === content) {
    return false;
  }

  await fs.writeFile(filePath, content, "utf8");
  return true;
}

async function readFileOrNull(filePath) {
  try {
    return await fs.readFile(filePath, "utf8");
  } catch (error) {
    if (error && error.code === "ENOENT") {
      return null;
    }
    throw error;
  }
}

async function exists(filePath) {
  try {
    await fs.access(filePath);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  const [indiaHtml, saudiEnglishHtml, saudiArabicHtml] = await Promise.all([
    readFileOrNull(INDIA_SOURCE_PATH),
    readFileOrNull(SAUDI_ENGLISH_SOURCE_PATH),
    readFileOrNull(SAUDI_ARABIC_SOURCE_PATH),
  ]);

  if (!indiaHtml || !saudiEnglishHtml || !saudiArabicHtml) {
    const generatedExists = await exists(OUTPUT_PATH);
    if (generatedExists) {
      console.log(
        `Source HTML files are unavailable. Using existing ${path.relative(PROJECT_ROOT, OUTPUT_PATH)}`,
      );
      return;
    }

    throw new Error(
      [
        "Landing source files are missing and no generated bundle exists.",
        `Expected: ${path.relative(PROJECT_ROOT, INDIA_SOURCE_PATH)}`,
        `Expected: ${path.relative(PROJECT_ROOT, SAUDI_ENGLISH_SOURCE_PATH)}`,
        `Expected: ${path.relative(PROJECT_ROOT, SAUDI_ARABIC_SOURCE_PATH)}`,
        `Missing: ${path.relative(PROJECT_ROOT, OUTPUT_PATH)}`,
      ].join("\n"),
    );
  }

  await fs.mkdir(OUTPUT_DIR, { recursive: true });
  const source = buildModuleSource(indiaHtml, saudiEnglishHtml, saudiArabicHtml);
  const changed = await writeIfChanged(OUTPUT_PATH, source);

  if (changed) {
    console.log(`Generated ${path.relative(PROJECT_ROOT, OUTPUT_PATH)}`);
    return;
  }

  console.log(`No changes in ${path.relative(PROJECT_ROOT, OUTPUT_PATH)}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
